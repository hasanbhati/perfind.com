document.addEventListener('DOMContentLoaded', () => {
    const langLinks = document.querySelectorAll('.lang-link');
    
    // Set up language switcher
    langLinks.forEach(link => {
        link.addEventListener('click', () => {
            if (!link.classList.contains('current')) {
                const lang = link.textContent.includes('हिन्दी') ? 'hi' : 'en';
                changeLanguage(lang);
            }
        });
    });

    // Initialize with stored language preference or default to English
    const storedLang = localStorage.getItem('preferred-language');
    
    // Only change language if there's a stored preference
    if (storedLang) {
        changeLanguage(storedLang);
    } else {
        // Set default to English if no stored preference
        changeLanguage('en');
    }
});

function changeLanguage(lang) {
    // Update language links
    document.querySelectorAll('.lang-link').forEach(link => {
        link.classList.remove('current');
        if ((lang === 'hi' && link.textContent.includes('हिन्दी')) ||
            (lang === 'en' && link.textContent.includes('English'))) {
            link.classList.add('current');
        }
    });

    // Update translations
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            // Check if the translation contains HTML tags
            if (translations[lang][key].includes('<')) {
                element.innerHTML = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });

    // Store language preference
    localStorage.setItem('preferred-language', lang);
} 